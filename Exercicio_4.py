#1- Conversor de Moeda

real = float(input("inserir o valor em Reais: "))    #Insira o valor em Reais
dollar = float(5.20)     #Valor de um dolar em reais
euro = float(6.15)     #Valor de um euro em reais
dolares = round(real*dollar,2)
euros = round(real*euro,2)
print(f"Esse valor em Dolar é: US$ {dolares:.2f}")               #Resultado da converção em dolar
print(f"Esse valor em Euro é: EU {euros:.2f}")               #Resultado da converção em euro

#2- Calculadora de Desconto

produto = input("Insira o produto: ")          #insira o nome do produto
valor = float(input("Insira o valor unitario: "))      #insira o valor de cada produto
print("Valor do produto: R$",(valor))               #valor original do produto
desconto = valor*0.20                            #desconto aplicado em cima do valor
print(f"Valor do Desconto: R$ {valor:.2f} - 20% = R$ {valor - desconto:.2f}")      #valor final da compra

#3- Calculadora de Média Escolar

a = float(input("Insira a nota 1: "))       #Valor da nota 1
b = float(input("Insira a nota 2: "))       #Valor da nota 2
c = float(input("Insira a nota 3: "))       #Valor da nota 3
media = (a+b+c)/3
print(f"A media escolar é {media:.2f}")     #Resultado da media das tres notas

#4- Calculadora de Consumo de Combustível
km = float(input("Insira a distancia pedorrida: "))       #Valor da distancia pecorrida em Kilometros
litros = float(input("Insira a quantidade de litros: "))       #Valor dos litos abastecidos
consumo = (km/litros)
print(f"A media de consumo da viagem é {consumo:.2f}Km/L")     #Resultado da media de consumo
